/**
 * Returns a movie character username based on the current date.
 * The same character is shown for the entire day, changing at midnight.
 */
export function getDailyPlaceholder(): string {
  const now = new Date();
  const daysSinceEpoch = Math.floor(now.getTime() / (1000 * 60 * 60 * 24));
  return movieCharacterUsernames[daysSinceEpoch % movieCharacterUsernames.length];
}
